    /**
     * Regression guard for requirement 4: when only a blank-UOM price exists, it applies to
     * every UOM variant of the item/variant regardless of the product's own UOM.
     */
    public function testGetPriceAppliesBlankUomToAllUomVariants(): void
    {
        $this->store->method('getBaseCurrencyCode')->willReturn('USD');
        $this->store->method('getWebsiteId')->willReturn(1);
        // Two blank-UOM candidates so the single-candidate short-circuit does not fire and the
        // UOM filter/selectBestPrice path is genuinely exercised for a COLLECTION product.
        $this->replicationHelper->method('getUomCodeGivenDescription')
            ->with('Collection', 1)
            ->willReturn('COLL');

        $blankA = $this->makeReplPrice(['id' => 1, 'unitOfMeasure' => '', 'startingDate' => '2026-01-01']);
        $blankB = $this->makeReplPrice(['id' => 2, 'unitOfMeasure' => '', 'startingDate' => '2026-06-01']);
        $blankA->method('getCurrencyCode')->willReturn('');
        $blankB->method('getCurrencyCode')->willReturn('');
        $blankA->method('getLineNumber')->willReturn(1);
        $blankB->method('getLineNumber')->willReturn(2);

        $replItemPriceList = [
            '40020-000-A' => $blankA,
            '40020-000-B' => $blankB,
        ];

        $product = $this->makeProduct('40020', '000', 'Collection');

        $result = $this->syncPrice->getPrice($product, $replItemPriceList);

        // Both survive the UOM filter (blank UOM is universal); selectBestPrice picks the
        // most-recent StartingDate (blankB, 2026-06-01). The key assertion is that a
        // blank-UOM price is returned for a COLLECTION product, never null.
        $this->assertSame($blankB, $result);
    }

    /**
     * Regression guard for the critical fix: when the ONLY currently-valid price row is
     * UOM-specific for a different UOM than the requesting product (a lone PACK price, product
     * is PCS) and there is no blank-UOM catch-all at all, getPrice() must return null rather
     * than bleeding the PACK price onto the PCS product. Against the pre-fix code the
     * count($candidates) === 1 short-circuit returned the PACK price before the UOM filter ran.
     */
    public function testGetPriceReturnsNullForSingleCrossUomCandidate(): void
    {
        $this->store->method('getBaseCurrencyCode')->willReturn('USD');
        $this->store->method('getWebsiteId')->willReturn(1);
        $this->replicationHelper->method('getUomCodeGivenDescription')
            ->with('Pieces', 1)
            ->willReturn('PCS');

        // Exactly one candidate, UOM-specific for PACK, no blank-UOM entry.
        $pack = $this->makeReplPrice(['id' => 2, 'unitOfMeasure' => 'PACK']);

        $replItemPriceList = [
            '40020-000-PACK' => $pack,
        ];

        $product = $this->makeProduct('40020', '000', 'Pieces');

        $result = $this->syncPrice->getPrice($product, $replItemPriceList);

        $this->assertNull($result);
    }
