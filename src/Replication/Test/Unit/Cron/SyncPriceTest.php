    private $replPriceRepository;

    public function setUp(): void
    {
        $this->objectManager = new ObjectManager($this);

        $this->lsr = $this->createMock(LSR::class);
        // Concrete Store (not StoreInterface) so getBaseCurrencyCode() — used by getPrice()
        // via selectBestPrice() — is mockable; the interface does not declare it.
        $this->store = $this->getMockBuilder(Store::class)
            ->disableOriginalConstructor()
            ->onlyMethods(['getId', 'getWebsiteId', 'getBaseCurrencyCode'])
            ->getMock();
        $this->replicationHelper = $this->createMock(ReplicationHelper::class);
        $this->replPriceRepository = $this->createMock(ReplPriceRepository::class);

        // "Current date" is 2026-07-15; convert() echoes back the date part it is handed so
        // isValidPrice()/isFuturePrice() perform plain Y-m-d string comparisons deterministically.
        $this->replicationHelper->method('getCurrentDate')->willReturn(self::CURRENT_DATE);
        $this->replicationHelper->method('convertDateTimeIntoCurrentTimeZone')
            ->willReturnCallback(fn($dateTime, $format = null, $useTimeZone = true) => substr((string)$dateTime, 0, 10));

        // ProductCreateTask's constructor calls $filesystem->getDirectoryWrite(...) and assigns the
        // result to a typed WriteInterface property, so a bare auto-mock (returning null) trips a
        // TypeError. Supply a Filesystem mock that returns a WriteInterface mock instead.
        $filesystem = $this->createMock(Filesystem::class);
        $filesystem->method('getDirectoryWrite')->willReturn($this->createMock(WriteInterface::class));

        $this->syncPrice = $this->objectManager->getObject(SyncPrice::class, ['filesystem' => $filesystem]);
        // These are all public properties inherited from ProductCreateTask.
        $this->syncPrice->lsr = $this->lsr;
        $this->syncPrice->store = $this->store;
        $this->syncPrice->replicationHelper = $this->replicationHelper;
        $this->syncPrice->replPriceRepository = $this->replPriceRepository;
    }

    /**
     * Build a mock ReplPrice-like object exposing the getters/setters the production code calls.
     *
     * @param array $data
     * @return ReplPrice|MockObject
     */
    private function makeReplPrice(array $data)
    {
        $defaults = [
            'id'            => 1,
            'itemId'        => '40020',
            'variantId'     => '',
            'priceListCode' => 'PL01',
            'status'        => 'Active',
            'startingDate'  => '',
            'endingDate'    => '',
            'unitOfMeasure' => '',
            'saleCode'      => '',
        ];
        $data = array_merge($defaults, $data);

        $price = $this->createMock(ReplPrice::class);
        $price->method('getId')->willReturn($data['id']);
        $price->method('getItemId')->willReturn($data['itemId']);
        $price->method('getVariantId')->willReturn($data['variantId']);
        $price->method('getPriceListCode')->willReturn($data['priceListCode']);
        $price->method('getStatus')->willReturn($data['status']);
        $price->method('getStartingDate')->willReturn($data['startingDate']);
        $price->method('getEndingDate')->willReturn($data['endingDate']);
        $price->method('getUnitOfMeasure')->willReturn($data['unitOfMeasure']);
        $price->method('getSaleCode')->willReturn($data['saleCode']);

        // resetNonWinnerPrices() reads the current processed/is_updated state via getData()
        // for its idempotency short-circuit. Only stub getData() when the caller supplies
        // those keys, so existing tests (which do not) keep the default null (=> not already
        // requeued) and continue to expect a save().
        if (array_key_exists('processed', $data) || array_key_exists('is_updated', $data)) {
            $stateData = [
                'processed'  => $data['processed'] ?? null,
                'is_updated' => $data['is_updated'] ?? null,
            ];
            $price->method('getData')->willReturnCallback(
                fn($key) => $stateData[$key] ?? null
            );
        }

        return $price;
    }

    /**
     * @param array $items
     * @return SearchResults|MockObject
     */
    private function makeSearchResults(array $items)
    {
        $results = $this->createMock(SearchResults::class);
        $results->method('getItems')->willReturn($items);
        return $results;
    }

    /**
     * @param SyncPrice $instance
     * @param object $winner
     * @return void
     */
    private function invokeResetNonWinnerPrices($instance, $winner): void
    {
        // No store price groups configured => isSaleCodeAllowedForStore() short-circuits to true
        // without hitting getStorePriceGroups()'s ObjectManager-backed ReplStore lookup (which is
        // unavailable in a unit context). Seed the private cache directly via reflection.
        $prop = new ReflectionProperty(SyncPrice::class, 'storePriceGroups');
        $prop->setAccessible(true);
        $prop->setValue($instance, []);

        $method = new ReflectionMethod(SyncPrice::class, 'resetNonWinnerPrices');
        $method->setAccessible(true);
        $method->invoke($instance, $winner);
    }

    /**
     * A currently-active short-window winner (15–16 Jul) must re-queue a longer-window
     * non-winner (1 Jan–1 Dec 2026) that is itself still valid, by saving it with processed=0.
     */
    public function testResetNonWinnerPricesRequeuesStillValidDatedNonWinner(): void
    {
        $this->store->method('getId')->willReturn(1);
        $this->store->method('getWebsiteId')->willReturn(1);
        $this->lsr->method('getStoreConfig')->willReturn('S0001');
        $this->replicationHelper->method('buildCriteriaForDirect')->willReturn($this->createMock(SearchCriteriaInterface::class));

        $winner = $this->makeReplPrice([
            'id'           => 10,
            'startingDate' => '2026-07-15',
            'endingDate'   => '2026-07-16',
        ]);
        // Non-winner: dated but currently valid (window covers 2026-07-15).
        $nonWinner = $this->makeReplPrice([
            'id'           => 20,
            'startingDate' => '2026-01-01',
            'endingDate'   => '2026-12-01',
        ]);

        $expectedSets = [];
        $nonWinner->expects($this->exactly(2))
            ->method('setData')
            ->willReturnCallback(function ($key, $value) use (&$expectedSets) {
                $expectedSets[$key] = $value;
            });

        $this->replPriceRepository->method('getList')
            ->willReturn($this->makeSearchResults([$nonWinner]));
        // The still-valid dated non-winner must be re-queued.
        $this->replPriceRepository->expects($this->once())
            ->method('save')
            ->with($nonWinner);

        $this->invokeResetNonWinnerPrices($this->syncPrice, $winner);

        $this->assertSame(0, $expectedSets['processed']);
        $this->assertSame(1, $expectedSets['is_updated']);
    }

