/**
 * Unit test coverage for SyncPrice::resetNonWinnerPrices() and SyncPrice::getPrice()
 * (work item #84363 — requeue still-valid dated fallback prices + fix cross-UOM price bleed).
 *
 * SyncPrice extends ProductCreateTask (large constructor), so the instance is built via
 * ObjectManager::getObject() with an autogenerated set of mocked collaborators; the specific
 * public properties the methods under test rely on ($lsr, $store, $replicationHelper, $logger,
 * $replPriceRepository) are then overwritten with controllable mocks — same pattern as
 * ReplicationHelperTest/LSRTest in this module. resetNonWinnerPrices() is private, so it is
 * invoked via reflection without changing production visibility; getPrice() is public.
 *
 * "Current date" is pinned to 2026-07-15 by stubbing ReplicationHelper::getCurrentDate() and
 * making convertDateTimeIntoCurrentTimeZone() echo back the Y-m-d portion of its argument, so
 * isValidPrice()/isFuturePrice() date maths is deterministic and wall-clock independent.
 */
class SyncPriceTest extends TestCase
{
